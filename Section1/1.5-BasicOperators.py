# Basic arithmetic

# Set the ShBang
#!/usr/local/bin/python
print(2+4)
print(3-5)
print(2--5)
print(3*8)

# 2 to the power of 4
print(2**4)

print(7 / 3)
print(7.0 / 3)

# Force integer division by using //
print(7 // 3)

print(7.0 // 3)


# Modulus operator
print(8 % 5)  # returns the remainder
print(8 % 4)
